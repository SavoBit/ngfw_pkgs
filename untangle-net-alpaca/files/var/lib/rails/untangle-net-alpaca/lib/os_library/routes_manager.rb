class OSLibrary::RoutesManager < Alpaca::OS::ManagerBase
  include Singleton

  ConfigFile = "/etc/untangle-net-alpaca/routes"

  def get_active
    results = []
    netstat = `netstat -rn | grep -v dummy0`.split( "\n" )
    number_of_heading_lines = 2
    netstat = netstat.slice( number_of_heading_lines, netstat.length )
    netstat.each do |entry|
      items = entry.split
      results << { :columns => [ { :value => items[0] }, #target
                                 { :value => items[2] }, #netmask
                                 { :value => items[1] }, #gateway
                                 { :value => "" },
                                 { :value => items[7] } ] } #iface
    end
    return results
  end



  def register_hooks
#    os["network_manager"].register_hook( -100, "routes_manager", "write_files", :hook_commit )
  end
  
  def hook_commit
    network_routes = NetworkRoute.find( :all )
    
    cfg = []

    network_routes.each do |network_route|
          cfg << "route add -net " + network_route.target + " netmask " + network_route.netmask + " gw " + network_route.gateway + " # " + network_route.name
    end
    
    os["override_manager"].write_file( ConfigFile, header, "\n", cfg.join( "\n" ), "\n" )
  end
  
  def header
    <<EOF
#!/bin/bash
## #{Time.new}
## Auto Generated by the Untangle Net Alpaca
## If you modify this file manually, your changes
## may be overriden
EOF
  end
end
